# Ask the user for input--dog's age:
user_regular_age = int(input("Enter your dogs age:")) # Here, we are converting the number input from string to an integer, and then assigning it to a variable.
# Perform the calculation:
dogs_human_age = user_regular_age * int(7) # Here, we are converting the string number to an integer number, and then assigning it to a variable.
# Display the result:
print(f"Your dog is {dogs_human_age} years old in human age!") # Here, we are using the f-string method to interpolate strings with variables.



